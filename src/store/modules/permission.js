import { asyncRoutes, constantRoutes } from '@/router'

/**
 * Use meta.role to determine if the current user has permission
 * @param roles
 * @param route
 */
function hasPermission(permissions, route) {
  if (route.meta && route.meta.permissions && route.meta.appLabel && permissions[route.meta.appLabel]) {
    var perms = []
    const applable = route.meta.appLabel.split('.')
    route.meta.permissions.forEach(p => {
      perms.push(p + '_' + applable[1].toLowerCase())
    })
    const dd = permissions[route.meta.appLabel].filter(v => perms.includes(v))
    return dd.length > 0
  } else {
    return true
  }
}

/**
 * Filter asynchronous routing tables by recursion
 * @param routes asyncRoutes
 * @param permissions
 */
export function filterAsyncRoutes(routes, permissions) {
  const res = []

  routes.forEach(route => {
    const tmp = { ...route }
    if (hasPermission(permissions, tmp)) {
      if (tmp.children) {
        tmp.children = filterAsyncRoutes(tmp.children, permissions)
      }
      res.push(tmp)
    }
  })

  return res
}

